package Contact;

public class Contact {
    public String nom;
    public String cognom;
    public long telefon;
    public String direccio;

    public Contact(String nom,String cognom,long telefon,String direccio) {
        this.nom = nom;
        this.cognom = cognom;
        this.telefon = telefon;
        this.direccio = direccio;
    }
}
